void findXMASCount();
vector<vector<int>> matMatched;
int main(){
    string str;
    while(cin >> str){
        mat.push_back(str);
    }

    for(int i=0;i<mat.size();i++){
        vector<int> temp;
        for(int j=0;j<mat[i].size();j++){
            temp.push_back(0);
        }
        matMatched.push_back(temp);
    }

    findXMASCount();
    return 0;
}

void findXMASCount(){
    int counter = 0;
    for(int i=0;i<mat.size();i++){
        for(int j=0;j<mat[i].size();j++){
            if(mat[i][j] == 'M'){
                for(int z=0;z<4;z++){
                    findXMAS(i,j,1, z);
                }
            }
        }
    }

    for(int i=0;i<mat.size();i++){
        for(int j=0;j<mat[i].size();j++){
            if(mat[i][j] == 'A'){
                counter += matMatched[i][j]/2;
            }
        }
    }


    cout << "Count: " << counter << endl;
}

int findXMAS(int x, int y, int pos, int direction){
    if(pos >= word.size()){
        matMatched[x][y]++;
        return 1;
    }

    int counter = 0;
    int xx = x + dx[direction];
    int yy = y + dy[direction];
    if(xx >=0 && xx < mat.size() && yy >= 0 && yy < mat[y].size()){
        if(mat[xx][yy] == word[pos]){
            int count = findXMAS(xx, yy, pos+1, direction);
            if(count >= 1){
                matMatched[xx][yy]+=count;
            }
            counter += count;
        }
    }

    return counter;
}

